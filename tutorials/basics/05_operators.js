// Types Of Operators ------------ //

// Arithmetic Operators ----- //

// Examples ðŸ‘‡

let val_1 = 10;
let val_2 = 5;

// +
console.log(val_1 + val_2);

// -
console.log(val_1 - val_2);
console.log(val_2 - val_1);

// *
console.log(val_1 * val_2);

// /
console.log(val_1 / val_2);

// /
console.log(val_1 % val_2);

// **
console.log(val_2 ** 3);

// ++
console.log(++val_2);

// --
console.log(--val_2);

// Assignment Operators ----- //

// Simple Include " = " Sign after Arithmetic Operator. Remember It's Not Working With ++ and --

let num_1 = 2;
let num_2 = 8;

// +=
console.log((num_1 += num_2));

let num_3 = 2;
let num_4 = 8;

// -=
console.log((num_4 -= num_3));

let num_5 = 2;
let num_6 = 8;

// /=
console.log((num_6 /= num_5));

let num_7 = 2;
let num_8 = 8;

// **=
console.log((num_8 **= num_7));

let num_9 = 2;
let num_10 = 8;

// %=
console.log((num_10 %= num_9));
